    /**
     * Konstruktor klasy Map.
     * Tworzy obiekt klasy Map na podstawie określonego poziomu.
     *
     * @param level Poziom mapy (1, 2 lub inny).
     */
    public Map(int level) {
        if (level == 1) {
            mesh = mesh1;
        } else if (level == 2) {
            mesh = mesh2;
        } else {
            mesh = mesh3;
        }
        generateMapStruct();
    }

    /**
     * Generuje strukturę mapy na podstawie ustawionego wcześniej poziomu.
     * Tworzy kafelki dla ścian i podłoża na podstawie wartości w tablicy mesh.
     * Kafelki są reprezentowane jako prostokątne obszary o wymiarach 32x32 piksele.
     */
    public void generateMapStruct() {
        for (int i = 0; i < Const.gWidth / 32; i++) {
            for (int j = 0; j < (Const.gHeight - Const.barHeight) / 32; j++) {
                if (mesh[j][i] == 1) {
                    // Tworzenie kafelka ściany
                    wallTiles.add(new Rectangle());
                    wallTiles.get(wallTiles.size() - 1).x = i * 32;
                    wallTiles.get(wallTiles.size() - 1).y = j * 32;
                    wallTiles.get(wallTiles.size() - 1).width = 32;
                    wallTiles.get(wallTiles.size() - 1).height = 32;
                } else {
                    // Tworzenie kafelka podłoża
                    groundTiles.add(new Rectangle());
                    groundTiles.get(groundTiles.size() - 1).x = i * 32;
                    groundTiles.get(groundTiles.size() - 1).y = j * 32;
                    groundTiles.get(groundTiles.size() - 1).width = 32;
                    groundTiles.get(groundTiles.size() - 1).height = 32;
                }
            }
        }
    }
